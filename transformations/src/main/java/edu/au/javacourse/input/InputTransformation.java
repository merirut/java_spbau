package edu.au.javacourse.input;

public class InputTransformation {

	private String type;
	private double kx;
	private double ky;
	private double dx;
	private double dy;
	private double alpha;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public double getKx() {
		return kx;
	}

	public void setKx(double kx) {
		this.kx = kx;
	}

	public double getKy() {
		return ky;
	}

	public void setKy(double ky) {
		this.ky = ky;
	}

	public double getDx() {
		return dx;
	}

	public void setDx(double dx) {
		this.dx = dx;
	}

	public double getDy() {
		return dy;
	}

	public void setDy(double dy) {
		this.dy = dy;
	}

	public double getAlpha() {
		return alpha;
	}

	public void setAlpha(double alpha) {
		this.alpha = alpha;
	}
}
